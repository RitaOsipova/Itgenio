kinder = int(input('Wie viele Kinder sind in der Klasse? '))
apple = int(input('Wie viele Äpfel waren in der Kiste? '))

result = apple // kinder
rest = apple % kinder

print(f'Es sind {kinder} Kinder in der Klasse.')
print(f'Es sind {apple} Äpfel in der Kiste.')
print(f'Jeder bekommt {result} Äpfel.')
print(f'Es bleiben {rest} Äpfel übrig.')

